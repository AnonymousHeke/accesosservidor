


public class Acceso
{
    
    
    int anno;
    int mes;
    int dia;
    int hora;
    int minuto;
    public Acceso (int anno, int mes, int dia, int hora, int minuto)
    {
        this.anno = anno;
        this.mes = mes;
        this.dia = dia;
        this.hora = hora;
        this.minuto = minuto;
    }
    
    public int getHora()
    {
        return hora;
    }
}